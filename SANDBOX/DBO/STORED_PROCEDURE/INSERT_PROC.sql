CREATE OR REPLACE PROCEDURE "DBO"."INSERT_PROC"()
RETURNS VARCHAR(16777216)
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN
 truncate table TEST.YES;
 
 INSERT INTO TEST.YES
 (
   PROD_ID,
   PROD_NAME ,
   CATAGORY,
   RATING,
   Indicater
 )
 SELECT
   PROD_ID,
   PROD_NAME ,
   CATAGORY,
   RATING,
   Indicater
   FROM TEST.PROD_TAB;
END;
';